#addition of elements into lists

#Can be done in 2 ways

#Using insert function or append function

#insert(i,value) inserts value in ith index of the list.

#append(value) inserts value at the end of the list

l=[1,2,3]

l.insert(2,4)

print(l)    #[1,2,4,3]

l.append(5)

print(l)    #[1,2,4,3,5]

l.sort()



